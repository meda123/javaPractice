public class For {
    public static void main (String[] args) {
        for ( int waterLevel = 0; waterLevel < 7; waterLevel ++) {

            System.out.println("The pool's water leve is at " + waterLevel +" feet.");
        }
    }
}


/*

A For loop in Java is composed to three parts 
1) Initialization: The int variable named counter is initialized to the value 0

2) Test condition: The Boolean expression counter < 5 is a conditional statement
evaluated before the code inside the control statement is run every loop

3) Increment: Each time the loop completes, the increment statement is run. The 
statement counter++ is the same as counter = counter + 1; 



*/